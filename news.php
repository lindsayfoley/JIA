			<div class="lightbox-background">
				<h4>Jamie's Home</h4>
				<p>The sight of children begging in the street has moved us to build a children's home in Parika. After securing a two acre plot of land in 2007 we have obtained planning 
				permission for the home which we are modelling on 'Operation Smile' in Trinidad. The concept is to have small houses with resident house mothers who will look after six to 
				eight children living with them.</p>
				<h4>Clearing the Land</h4>
				<p>Initially we had to cut drainage channels to help dry out the ground and allow building work to commence. There is a dirt road giving access to the area, but it was necessary 
				to build a small bridge from the road over the drainage channel adjacent to it, in order to get access to the land. Everything grows apace in this environment with its 
				plentiful sunshine and rain, so clearing back the grass and undergrowth was quite a job before we could even start the foundations.</p>
