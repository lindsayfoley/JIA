				<h4>Doors &amp; Windows</h4>
				<p>Small increments of progress as funds came in, allowed us to fit windows with mosquito netting screens, and have the front and back doors made from the local hardwood - 
				purpleheart. (they look great). In the interior, the floors have been levelled, and the walls rendered. We have used concrete as much as possible to give the building longevity, 
