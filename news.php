				<h4>The wrong kind of planning permission!</h4>
				<p>We were delayed in our progress after being told that we had the 'wrong kind' of planning permission! Apparently planning permission for an orphanage needed to be granted 
				through Georgetown Planning Department, although we had already been granted permission by the local council department. After redrawing and resubmitting the plans to Georgetown, 
				we were once more on the way... </p>
				<h4>The Roof is on!</h4>
				<p>This is the Southern end of the building, and one can see the small veranda to the rear which leads into the back of the kitchen area. Having porches at both the front and rear 
				of the building gives a shaded area to sit out, for both the rising and setting sun.</p>
