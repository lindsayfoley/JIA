			<div class="lightbox-background">
				<h4>Getting Started with the Building</h4>
				<p>In 2009 we started laying the foundations for the first building. The floor level of the house needed to be built up because of the risk of local flooding with the high level 
				of rainfall. Along side we have constructed a septic tank to receive the drainage from the building. We plan to collect rain water from the building's roof but will probably need
				 to supplement this with a well at a later stage.</p>
				<h4>Further Works</h4>
				<p>Once the floor of the building was laid we decided that the location of the entry porch needed to be changed because of the narrowness of the strip of land that it is being
				 built on. By autumn 2010 we have got the walls up so that the configuration of the rooms is apparent, and we plan to build a front and rear porch and get the roof on next.</p>
			</div>
